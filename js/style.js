var main = function() {
  /*open Menu */
  $('.icon-menu').click(function() {
    $('.menu').animate({
      left: "0px"
    }, 200);

    $('body').animate({
      left: "285px"
    }, 200);
  });

  /* Close Menu  */
  $('.icon-close').click(function() {
    $('.menu').animate({
      left: "-285px"
    }, 200);
      

    $('body').animate({
      left: "0px"
    }, 200);
  });
};


$(document).ready(main);